package mhir.main.aetherling

import com.typesafe.scalalogging.Logger
import mhir.gen.TestPassed
import mhir.gen.verilog.{
  VerilogProjectInitializer,
  VerilogTestRunner,
  VerilogTestbenchGenerator
}
import mhir.gen.vhdl.{VhdlTestRunner, VhdlTestbenchGenerator}
import mhir.logging.time
import mhir.main.shared.{CompilerOptions, VhdlTarget}
import mhir.optimize.OptimizerOptions
import mhir.testing.HardwareTest
import org.scalatest.funsuite.AnyFunSuite
import org.slf4j.event.Level

/** Tests that the VHDL designs produced by [[mhir.main.aetherling.Compiler]]
  * behave the same as the Verilog designs generated by
  * [[https://dl.acm.org/doi/10.1145/3385412.3385983 Aetherling]].
  */
@HardwareTest
class AetherlingBenchmarkTests extends AnyFunSuite {
  private val AetherlingBenchmarksDir =
    os.pwd / "src" / "test" / "resources" / "aetherling_benchmarks" / "original"
  private val VerilogBenchmarksDir =
    os.pwd / "src" / "test" / "resources" / "aetherling_benchmarks" / "verilog"
  private val VhdlDir = os.pwd / "src" / "test" / "vhdl"
  private val VerilogDir = os.pwd / "src" / "test" / "verilog"
  private val TimeLimit: String = "0" // 0 means no timeout

  private val AllBenchmarks: Seq[String] =
    os.list(AetherlingBenchmarksDir).map(_.baseName)
  private val BenchmarksToRun: Seq[String] =
    AllBenchmarks

  private implicit val logger: Logger = Logger(getClass.getName)

  test("AllBenchmarks") {
    assert(BenchmarksToRun == AllBenchmarks)
  }

  for (benchName <- BenchmarksToRun) {
    test(s"$benchName:vhdl") {
      if (!benchName.startsWith("smallsharpen")) ???
      assume(!benchName.startsWith("bigcamera")) // Too slow
      val io = AetherlingBenchmarkIO.vhdlIO(benchName)
      val inFile = AetherlingBenchmarksDir / s"$benchName.txt"
      val outDir = VhdlDir / "aetherling" / s"${benchName}_test"
      val args = Args(
        inFile = inFile,
        options = CompilerOptions(
          showFinal = false,
          target = VhdlTarget(outDir = outDir, overwrite = true),
          optFlags = OptimizerOptions.all(assumeThroughputsMatch = true)
        )
      )
      Compiler.compile(args)
      time("generating VHDL testbench", Level.INFO) {
        VhdlTestbenchGenerator.makeFileBasedTestbench(io = io, dir = outDir)
      }
      val result = time("running VHDL simulation", Level.INFO) {
        VhdlTestRunner.testExistingProject(outDir, timeLimit = TimeLimit)
      }
      assert(result == TestPassed)
    }

    test(s"$benchName:verilog") {
      ???
      assume(!benchName.startsWith("bigcamera")) // Too slow
      val io = AetherlingBenchmarkIO.verilogIO(benchName)
      val projectDir = VerilogDir / s"aetherling" / s"${benchName}_test"
      VerilogProjectInitializer.initProj(
        projectDir,
        VerilogBenchmarksDir / s"$benchName.v",
        overwrite = true
      )
      time("generating Verilog testbench", Level.INFO) {
        VerilogTestbenchGenerator.makeFileBasedTestbench(io, projectDir)
      }
      val result = time("running Verilog simulation", Level.INFO) {
        VerilogTestRunner.testExistingProject(projectDir, timeLimit = TimeLimit)
      }
      assert(result == TestPassed)
    }
  }
}
